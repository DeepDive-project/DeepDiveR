#' Carnivore data
#'
#' A subset of occurrence data from Faurby et al 2019.
#'
#' @format ## `carnivora`
#' A data frame with 11,309 rows and 5 columns:
#' \describe{
#'   \item{Taxon}{Taxonomic name}
#'   \item{Area}{The discrete region in which the taxon was found}
#'   \item{MinAge}{The youngest age estimate of the fossil occurrence in millions of years}
#'   \item{MaxAge}{The oldest age estimate of the fossil occurrence in millions of years}
#'   \item{Locality}{A unique identifier for the locality at which the fossil was recorded}
#'   ...
#' }
#' @source <https://www.biorxiv.org/content/10.1101/755207v1.abstract>